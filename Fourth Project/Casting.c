int casting()
{
    int iNum1 = 5, iNum2 = 2;

    //Operación entre 2 tipos de variables va a dar como resultado una variable del mismo tipo
    printf("División entre 2 enteros: %i \n", iNum1 / iNum2);
    //INT * FLOAT = INT
    printf("División entre # entero y # decimal: %f \n \n", (float)iNum1 / iNum2);
    return 0;
}
